    void urunGoster(int index) {
        try {
            satis_stok.setText(Integer.toString(urunmanager.getUrunler().get(index).getStok_miktari()));
            satis_kod.setText(Integer.toString(urunmanager.getUrunler().get(index).getUrun_kodu()));
            satis_ad.setText(urunmanager.getUrunler().get(index).getUrun_adi());
            satis_marka.setText(urunmanager.getUrunler().get(index).getUrun_marka());
            satis_model.setText(urunmanager.getUrunler().get(index).getUrun_model());
            satis_fiyat.setText(Float.toString(urunmanager.getUrunler().get(index).getUrun_fiyat()));
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    

