    private void btn_satis_kaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_satis_kaydetActionPerformed
    
        System.out.println("kaydet butonuna basıldı");
        try {
            Connection cn = helper.getConnection();      
            PreparedStatement ps = cn.prepareStatement("update stokyeni SET stok_miktari=? WHERE urun_kodu=?");

         
        int stokmik = Integer.parseInt(satis_stok.getText()); //stok miktari
        int satisadet = Integer.parseInt(satis_adet.getText()); //satilan adet
                                                                //stok mik- satilan adet olarak güncellenecek yeni stok mik.
            ps.setInt(1,  stokmik-satisadet);
            ps.setInt(2,  Integer.parseInt(satis_kod.getText()));
            
            JOptionPane.showMessageDialog(null, "Stok miktarı : " + stokmik);
            JOptionPane.showMessageDialog(null, "Satılacak adet : " + satisadet);

            JOptionPane.showMessageDialog(null, "Satış kodu : " + satis_kod.getText());
            ps.executeUpdate();
            //urunleri_tabloya_aktar();
                JOptionPane.showMessageDialog(null, "Satış Başarılı, tabloyu yenileyin");
                satis_kod.setText("");
                satis_adet.setText("");
                satis_ad.setText("");      //bu temizleme işlemini bir fonksiyona atayabiliriz.
                satis_marka.setText("");
                satis_model.setText("");
                satis_fiyat.setText("");
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btn_satis_kaydetActionPerformed

